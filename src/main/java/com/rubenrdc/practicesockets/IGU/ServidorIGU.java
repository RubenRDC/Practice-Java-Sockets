    public ServidorIGU() {
        initComponents();
        Thread r = new Thread(new Runnable() {
            @Override
            public void run() {
                try {
                    ServerSocket Ssocket = new ServerSocket(9999);
                    while (true) {
                        
                        Socket so = Ssocket.accept();
                        DataInputStream dataR = new DataInputStream(so.getInputStream());

                        String mensajeRecibido = dataR.readUTF();
                        jTextArea1.append(mensajeRecibido + "\n");
                        dataR.close();
                        so.close();
                    }

                } catch (IOException ex) {
                }
            }
        });
        r.start();
    }

